                        <div class="faq col-md-12">
                            <div class="question">
                                3. Do I need a partner to enroll?
                            </div>
                            <div class="answer">
                                <p>
                                    No, not at all. You don't need a partner to enroll. Normally, guys and ladies sign up as individuals but dance as partners in the class. You will rotate/switch with each other in the class so everyone can have a go.
                                </p>
                                <p>
                                    However, if you are signing up as a couple and you don't want to switch, we are fine. Just tell the teacher your preference of not rotating partners before each class.  
                                    
                                  </p>
                                <p>  Having said that, we highly encourage you to change partners, as you never know what you or your partner is missing out or doing wrong until you dance with different people. Dancing with everyone speeds and improves your abilities and gives you a true sense of partner dancing.
                                </p>
                            </div>
                        </div>
                        <div class="faq col-md-12">
                            <div class="question">
                                4. Do you have even numbers of males & females?
                            </div>
                            <div class="answer">
                                <p>
                                    We always keep an eye on the bookings but sometimes we don't have an equal number of males and females. 
                                    </p><p>
    It is something that you don't have to worry about because our classes are designed to rotate partners every few minutes.
    If you stay by yourself for a little while you will have a partner on the next rotation.
</p><p>
    We invite our students from other levels if the numbers are different. We do our best to get even numbers.
</p>
                            </div>
                        </div>
