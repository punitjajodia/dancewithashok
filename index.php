                            <div class="question">
                                6. What clothes/shoes should I wear?
                            </div>
                            <div class="answer">
                                <p>
                                    <strong>Clothes:</strong> Wear comfortable and clean clothes. We have separate changing rooms for male and female so it is also possible to change at the studio if you come directly from your workplace or college.

                                </p>
                                <p>
                                    <strong>Shoes:</strong> For the first few months, use flat shoes, sneakers, converse, party shoes that won't slip on the floor or grip too much. For ladies, heels with ankle straps are recommended as long as you are comfortable. Later on you can buy proper Jazz or Latin dance shoes. 
                                    </p><p>Dance shoes are a great investment as we have noticed that students with proper footwear tend to learn without any difficulties and injuries.</p> 
                           
                                <p>
                                    <strong>NOTE:</strong> Please remember that you are dancing with a partner so personal hygiene is important. Please use deoderant and breath fresheners. Don't take it personally if anyone suggests you to do so. If you smell good, it is always nice and pleasant.
                                </p>

                            </div>
