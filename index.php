                        <div class="faq col-md-12">
                            <div class="question">
                                2. Who can enroll for the classes ? Age bar?
                            </div>
                            <div class="answer">
                                <p>
                                    The classes are open to all, whether local or foreigner, teenager or adult.
                                </p>
                                <p>
                                    There is no age bar for the salsa classes. Age will mere define the limitation on difficulty level of moves you can learn and the ability to grasp things. Our students tend to range quite widely between late teens to those in their late 50's, with the vast majority falling between 16's to 45's.
                                </p>
                                <p>
                                    If you fall anywhere outside this box, we recommend you to take private classes so that we can adopt on your strengths and weaknesses. If you want to teach Salsa to your children, consider adding them to our children's private batch. Please <a href="#contacts">contact</a> the instructors for more suggestions.
                                </p>
                            </div>
